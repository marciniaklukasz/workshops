lista = ['zero', 'jeden', 'dwa', 'trzy', 'cztery', 'pięć', 'sześć', 'siedem', 'osiem', 'dziewięć']
print(lista)
# drugi element
print(lista[1])
# przedostatni element
print(lista[-1])
# elementy od trzeciego do siódmego (włącznie)
print(lista[2:8])
# co trzeci element
print(lista[::3])
# co drugi element licząc od końca
print(lista[::-2])

